places = [ 'nepal', 'india', 'turkey', 'mongolia', 'iceland' ]

print(places)
print('\n')

print(sorted(places)) #sorted the list without modifying the actual list
print('\n')

print(places) # showing the original list
print('\n')

places.reverse() # reversing the list
print(places) #printing the reversed list
print('\n')

places.reverse() # again reversing to change the order
print(places) # to show its back into original form
print('\n')

places.sort() # sorting the list
print(places)
print('\n')

places.sort() # sorting the list
print(places)
print('\n')


